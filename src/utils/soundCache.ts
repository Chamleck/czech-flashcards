// Звуки квізу. Реалізація — локальний нативний модуль modules/quiz-sounds на базі
// Android SoundPool (API, створений саме для коротких звукових ефектів).
//
// Історія, щоб не наступити знову: до цього звук був на expo-audio (ExoPlayer) і на
// expo-av — обидва є плеєрами загального призначення для музики/відео. Вони виконують
// важку ініціалізацію в момент play(), через що короткий звук (0.29 с) на першому тапі
// після холодного старту встигав програти в тишу. Розробники expo-audio самі це визнали
// (expo/expo#42900: "doesn't fit for sound effects or UI sound interaction like clicks").
// Жодні спроби вгадати таймінг з боку JS цього не лікували — бо проблема була не в
// таймінгу, а в неправильному класі інструмента.
//
// SoundPool декодує звук у PCM і тримає в пам'яті на етапі завантаження (старт
// застосунку), тож play() лише підмішує готові дані. Уся ініціалізація — у нативному
// OnCreate модуля, тому з боку JS тут не лишилось нічого: ні кешів, ні прогрівів,
// ні гейтів готовності. Цей файл — просто тонкий адаптер, щоб екрани залежали від
// власної абстракції застосунку, а не від шляху до модуля.

